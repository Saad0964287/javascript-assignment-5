let a=10;
document.write("Value of a is "+a+"<br>");
++a;
document.write("Value of ++a is "+a+"<br>");
document.write("Value of a is now "+a+"<br>");

document.write("Value of a++ is "+a+"<br>");
a++;
document.write("Value of a is now "+a+"<br>");

--a;
document.write("Value of --a is "+a+"<br>");
document.write("Value of a is now "+a+"<br>");

document.write("Value of a-- is "+a+"<br>");
a--;
document.write("Value of a is now "+a+"<br>");